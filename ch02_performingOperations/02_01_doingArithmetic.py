# Common operators
# +  Addition
# -  Subtraction
# *  Multiplication
# /  Division
# %  Modulo
# // Floor division
# ** Exponent

# Initialize Integer Values
a = 8
b = 2

# Operation Examples
print('Addition:\t', a, '+', b, '=', a + b)

print('Subtraction:\t', a, '-', b, '=', a - b)

print('Multiplication:\t', a, '*', b, '=', a * b)

print('Divison:\t', a, 'รท', b, '=', a / b)

print('Floor Division:\t', a, 'รท', b, '=', a // b)

print('Modulo:\t\t', a, '%', b, '=', a % b)

print('Exponent:\t ', a, '^2 = ', a ** b, sep='')